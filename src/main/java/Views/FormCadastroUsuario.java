    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (txtNome.getText().isEmpty() || txtEmail.getText().isEmpty() || new String(txtSenha.getPassword()).isEmpty()) {

                JOptionPane.showMessageDialog(null, "Não pode haver campos vazios");

            } else {
                Usuario user = new Usuario();

                user.setNome(txtNome.getText());
                user.setEmail(txtEmail.getText());
                user.setSenha(Arrays.toString(txtSenha.getPassword()));
                CadastrarUsuario cadastro = new CadastrarUsuario(user.getNome(), user.getEmail(), user.getSenha());
                dispose();
        }


    }//GEN-LAST:event_jButton1ActionPerformed

